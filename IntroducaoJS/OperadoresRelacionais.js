// Seção 1 - Maior (>)

let num1 = 15

if (num1 > 10) {
    console.log("O número é maior que 10")
}

console.log("--------------------------------------------------------")

//Seção 2 - Menor (<)
let num2 = 3

if (num2 < 5) {
    console.log("O número é menor que 5")
}

console.log("--------------------------------------------------------")

// Seção 3 - Maior ou Igual (>=)
let nota = 7

if (nota >= 7) {
    console.log("Aluno aprovado")
}

console.log("--------------------------------------------------------")

// Seção 4 - Menor ou Igual (<=)

let temperatura = 18

if (temperatura <= 20) {
    console.log("Temperatura baixa")
}

console.log("--------------------------------------------------------")

// Seção 5 - Igual (== ou ===)
let senha = 1234

if (senha === 1234) {
    console.log("Senha correta")
}

console.log("--------------------------------------------------------")

// Seção 6 - Diferente (!= ou !==)
let numero = 5

if (numero !== 0) {
    console.log("O número é diferente de zero")
}

console.log("--------------------------------------------------------")